                <p>JigJam is a multi-award winning quartet from the heart of the midlands in Ireland. Blending the best of traditional Irish music with bluegrass and Americana in a new genre which has been branded as ‘CeltGrass’, their onstage energy along with their virtuos musical ability has captivated audiences throughout the world. “Here’s an Irish band that’s going to impact the world as hard as Clancy Brothers or U2 if they get only half a chance.”</p>

                <p>Jamie McKeogh, Cathal Guinan and Daithi Melia all hail from Tullamore, Co.Offaly with Co. Tipperary born Gavin Strappe completing the quartet. All four members grew up immersed in Irish traditional music and culture which is reflected by the band collectively achieving over twenty All-Ireland titles at Fleadh Cheoil na hÉireann competitions. They have now developed their own unique style of music influenced by American Folk music whilst staying true to their Irish roots. Described as ‘The best Irish group so far in bluegrass’ this sharply dressed outfit deliver an energy-fuelled, foot-stomping live performance. All multi-instrumentalists, JigJam interchange between banjos, guitars, fiddles, mandolins and double bass onstage which creates an experience which is pleasing to both the eye and the ear. JigJam have recorded two studio albums to critical acclaim and have been compared to the Punch Brothers by the European Bluegrass Music Association. JigJam have made a huge impact on the Irish American circuit performing at all major festivals including the world-renowned Milwaukee Irish Fest as well as touring various parts of the UK and Europe. ‘The words sparkling, innovative and infectious come first to mind’ when you witness your first JigJam show. Miss this band at your peril.</p>




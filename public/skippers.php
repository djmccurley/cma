                <p class="feature">Skipper’s Alley are a 7-piece ensemble whose dynamic take on traditional Irish music has garnered wide critical praise and featured on such diverse stages as The Late Late Show, La Festival Interceltique de Lorient, and the Irish Embassy in Zambia.</p>

                <p>Described by The Irish Times as “gloriously three-dimensional”, the group spring from harp-accompanied Sean-Nós songs to feral, unkempt sets of reels, creating a sound that is both “scintillating” and “a model of tender delicacy” (David Kidman, The Living Tradition).</p>

                <p>Skipper's Alley first came to prominence in the summer of 2013 after winning the Loic Raison Trophy at Brittany's Festival Interceltique de Lorient, an award previously won by such established acts as Danú, Clannad, and The Bothy Band. In the same six-month period, the group launched an EP, made their live television debut (The Late Late Show) and were the subject of an hour-long radio special on RTÉ’s Céilí House. 2014 saw the band enjoy continued success with performances in the UK, mainland Europe and even a St. Patrick’s Day tour in Zambia, all ending with the release of their critically-acclaimed debut album ‘Skipper’s Alley’ (“★★★★”- The Irish Times).</p>

                <p>Today, the ensemble are a promising young act in the Irish traditional circuit and look forward to a host of national and international appearances throughout 2015.</>

                <p>“There is no doubt that this group will have an impact on the future of traditional Irish music.” - Kieran Hanrahan, Stockton’s Wing"</p>




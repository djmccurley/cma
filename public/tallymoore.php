                <p class="feature">Tallymoore is a contemporary Irish Folk band from Milwaukee, Wisconsin performing a range of Irish and Scottish folk tunes, ballads, reels and classic pub songs. The group also incorporates acoustic and vocal material from broader Celtic, traditional country, bluegrass, and Americana sources.</p> 

                <p>Established in 2012, Tallymoore has made appearances at major Irish music festivals across the United States including Milwaukee, Kansas City, Syracuse, and Indianapolis. They also perform regularly at popular bars and restaurants, and for Irish-related events and private engagements. Tallymoore released their second full-length album “Drive” in March of 2016.</p>

                <p>Tallymoore aims to breathe new life into Irish folk favorites. They are best known for their strong vocals and complex harmonic treatments of tunes, along with rich instrumentation featuring the ambitious and polished fiddle stylings of Erin Hoffmann. Their innovative arrangements of classic tunes have made them a favorite among festival goers. Their vocal harmony blend, distinctive arrangements, and strong musicianship have distinguished them on the Irish music scene.</p>

                <p>Tallymoore’s four core players are Sean Ward (mandolin), Erin Hoffmann (violin), Matt Voell (piano) and Dave Kennedy (guitar). They frequently perform with guest bassist, Karl Suechting.</p>

